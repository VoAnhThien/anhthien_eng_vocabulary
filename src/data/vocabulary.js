// Lấy sets của một category
export const getCategorySets = (categoryId) => {
  const category = VOCABULARY_DATA[categoryId];
  if (!category) return [];
  
  return Object.entries(category.sets).map(([name, words]) => ({
    name,
    words,
    count: words.length
  }));
};
// Utility function để lấy tất cả categories, bao gồm cả category custom
export const getCategories = () => {
  const categories = Object.keys(VOCABULARY_DATA).map(key => ({
    id: key,
    ...VOCABULARY_DATA[key].info,
    totalWords: Object.values(VOCABULARY_DATA[key].sets).reduce(
      (sum, words) => sum + words.length, 0
    ),
    setsCount: Object.keys(VOCABULARY_DATA[key].sets).length
  }));

  // Thêm category custom
  categories.push({
    id: 'custom',
    title: 'Tự tạo bộ từ',
    description: 'Tạo bộ từ vựng của riêng bạn',
    color: 'from-orange-500 to-red-500',
    totalWords: 'Tự nhập',
    setsCount: '∞'
  });

  return categories;
};